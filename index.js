class Breakfast {
    constructor(food, drink) {
        this.food = food
        this.drink = drink
    }
}

// LUNCH c l a s s 

class Lunch {
    constructor(salad, soup, drink) {
        this.salad = salad
        this.soup = soup
        this.drink = drink
    }
}

